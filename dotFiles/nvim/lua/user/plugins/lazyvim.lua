-- Bootstrap package manager (LazyVim)
local lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
if not vim.loop.fs_stat(lazypath) then
  vim.fn.system({
    "git",
    "clone",
    "--filter=blob:none",
    "https://github.com/folke/lazy.nvim.git",
    "--branch=stable", -- latest stable release
    lazypath,
  })
end
vim.opt.rtp:prepend(lazypath)
-- End Bootstrap

require("lazy").setup("user/plugins/lazy") -- any lua file in ~/.config/nvim/lua/user/plugins/lazy/*.lua will be automatically merged in the main plugin spec
